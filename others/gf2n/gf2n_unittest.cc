TEST(gf2n, MulThenModTest)
{
    int p, p1, p2, t;

    p1 = 0x57;  // x^6 + x^4 + x^2 + x + 1
    p2 = 0x83;  // x^7 + x + 1
    p  = 0x11b; // x^8 + x^4 + x^3 + x + 1

    t = gf2n_mul(p1, p2);
    EXPECT_EQ(0x2b79, t);

    t = gf2n_mod(t, p);
    EXPECT_EQ(0xc1, t);
}

TEST(gf2n, InvTest)
{
    int p1, p2;

    p1 = 0x57;  // x^6 + x^4 + x^2 + x + 1
    p2 = 0x11b; // x^8 + x^4 + x^3 + x + 1

    EXPECT_EQ(0xbf, gf2n_inv(p1, p2));
}

